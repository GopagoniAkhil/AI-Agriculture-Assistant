// Get current language (default to English)
let currentLanguage = localStorage.getItem('language') || 'en';

// ==========================================
// TRANSLATION FUNCTIONS
// ==========================================

// Get translated text with fallback to English
function t(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];
    
    // Try to get value in current language
    for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
            value = value[k];
        } else {
            // Fallback to English
            value = translations['en'];
            for (const k of keys) {
                if (value && typeof value === 'object' && k in value) {
                    value = value[k];
                } else {
                    console.warn(`Translation missing: ${key} in ${currentLanguage}`);
                    return key;
                }
            }
            return value || key;
        }
    }
    
    return value || key;
}

// Change language and update UI
function setLanguage(lang) {
    if (lang in translations) {
        currentLanguage = lang;
        localStorage.setItem('language', lang);
        console.log(`Language changed to: ${lang}`);
        updateUILanguage();
        return true;
    }
    console.warn(`Language not supported: ${lang}`);
    return false;
}

// Get current language
function getLanguage() {
    return currentLanguage;
}

// ==========================================
// COMPREHENSIVE UI UPDATE SYSTEM
// ==========================================

function updateUILanguage() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            if (el.hasAttribute('placeholder')) {
                el.placeholder = translation;
            } else {
                el.value = translation;
            }
        } else {
            el.textContent = translation;
        }
    });
    
    // Update specific elements by ID for non-standard cases
    updateSpecificElements();
    
    console.log(`UI updated for language: ${currentLanguage}`);
}

